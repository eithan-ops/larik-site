export function pickTrivia(cat: string, n: number): TriviaQ[] {
  const pool = cat === "mix" ? [...TRIVIA] : TRIVIA.filter((t) => t.cat === cat);
  const shuffled = [...pool].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(n, shuffled.length));
}
